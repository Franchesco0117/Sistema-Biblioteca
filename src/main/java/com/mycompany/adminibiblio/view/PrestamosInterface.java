    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        
        Prestamos prestamo = new Prestamos();
        prestamo.setId(Integer.parseInt(etId.getText()));
        prestamo.eliminarPrestamo(etId);
        prestamo.mostrarPrestamos(tbPrestamos);
        prestamo.limpiarCampos();
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnBuscarLibroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarLibroActionPerformed
        
        Prestamos prestamo = new Prestamos();
        boolean encontrado = prestamo.buscarPrestamo(etId, ComboBoxLibros, ComboBoxUsuarios, DateChooserPrestamo, DateChooserDevolucion, ComboBoxEstadoPrestamo, tbPrestamos);

        if (encontrado) {
            // El libro fue encontrado y los campos de texto fueron llenados
            JOptionPane.showMessageDialog(null, "Prestamo encontrado y campos llenados.");
        } else {
            JOptionPane.showMessageDialog(null, "Prestamo no encontrado");
        }
    }//GEN-LAST:event_btnBuscarLibroActionPerformed

