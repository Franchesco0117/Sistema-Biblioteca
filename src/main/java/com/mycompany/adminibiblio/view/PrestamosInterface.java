    private void btnInformeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInformeActionPerformed

        try {
            // Ruta del archivo .jasper compilado
            String reportPath = "C:\\Users\\cmpau\\OneDrive\\Documentos\\NetBeansProjects\\AdminiBiblio\\src\\main\\java\\com\\mycompany\\adminibiblio\\view\\PrestamoReport.jasper";

            // Parámetros de conexión a la base de datos
            String usuario = "root";
            String contrasenia = "franciscocastromurillo012";
            String bd = "library_management";
            String ip = "localhost";
            String puerto = "3306";
            String cadena = "jdbc:mysql://" + ip + ":" + puerto + "/" + bd;

            // Establecer la conexión con la base de datos
            try (Connection conn = DriverManager.getConnection(cadena, usuario, contrasenia)) {
                // Llenar el informe con datos
                JasperPrint jasperPrint = JasperFillManager.fillReport(reportPath, null, conn);

                // Mostrar el informe en una ventana
                JasperViewer viewer = new JasperViewer(jasperPrint, false);
                viewer.setVisible(true);
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error al generar el informe: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnInformeActionPerformed

