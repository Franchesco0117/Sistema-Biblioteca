    private void btnBuscarLibroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarLibroActionPerformed

        Editoriales editorial = new Editoriales();
        boolean encontrado = editorial.buscarEditorial(etId, etNombre, jYearChooserEditorial, ComboBoxPais, tbEditoriales);

        if (encontrado) {
            // El libro fue encontrado y los campos de texto fueron llenados
            JOptionPane.showMessageDialog(null, "Editorial encontrado y campos llenados.");
        } else {
            JOptionPane.showMessageDialog(null, "Editorial no encontrado");
        }
    }//GEN-LAST:event_btnBuscarLibroActionPerformed

    private void tbEditorialesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbEditorialesMouseClicked

        Editoriales editorial = new Editoriales();
        editorial.seleccionarEditorial(tbEditoriales, etId, etNombre, jYearChooserEditorial, ComboBoxPais);
    }//GEN-LAST:event_tbEditorialesMouseClicked

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

        Editoriales editorial = new Editoriales();

        editorial.insertarEditorial(etNombre, ComboBoxPais, jYearChooserEditorial);
        editorial.mostrarEditoriales(tbEditoriales);
        editorial.limpiarCampos(etNombre);
    }//GEN-LAST:event_btnGuardarActionPerformed

